package com.zxqy.xunilaidian.bean;

public class EventData {
    int alpha;
    int color;
    int baohedu;
    int type;
    int size;

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public int getAlpha() {
        return alpha;
    }

    public void setAlpha(int alpha) {
        this.alpha = alpha;
    }

    public int getColor() {
        return color;
    }

    public void setColor(int color) {
        this.color = color;
    }

    public int getBaohedu() {
        return baohedu;
    }

    public void setBaohedu(int baohedu) {
        this.baohedu = baohedu;
    }
}
